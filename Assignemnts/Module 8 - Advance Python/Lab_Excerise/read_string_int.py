# This code demonstrates how to read different types of input from the user in Python. It prompts the user to enter a string, an integer, and a float, and then prints the entered values to the console.

# without function
string_input = input("Enter a string: ")
int_input = int(input("Enter an integer: "))
float_input = float(input("Enter a float: "))

print("You entered:")
print("String:", string_input)
print("Integer:", int_input)
print("Float:", float_input)

