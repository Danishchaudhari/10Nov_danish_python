text = "Hello, Python Programming!" 

print("Original String:", text)


print("1. First 5 characters:", text[:5])            # From start to index 4
print("2. Characters from index 7 to 12:", text[7:13]) # From index 7 to 12
print("3. Last 10 characters:", text[-10:])          # From the 10th last to the end
print("4. Every 2nd character:", text[::2])          # Step of 2
print("5. Reverse the string:", text[::-1])          # Reverse the string
